package org.web.model;

public class Admine {
	
	private int admineId;
	private String admineName;
	private	String adminePass;
	
	
	public int getAdmineId() {
		return admineId;
	}
	public void setAdmineId(int admineId) {
		this.admineId = admineId;
	}
	public String getAdmineName() {
		return admineName;
	}
	public void setAdmineName(String admineName) {
		this.admineName = admineName;
	}
	public String getAdminePass() {
		return adminePass;
	}
	public void setAdminePass(String adminePass) {
		this.adminePass = adminePass;
	}

}
